/* El método reverse() invierte el orden de los elementos de un array.
El primer elemento pasa a ser el último y el último pasa a ser el primero */

console.log('TEST Array.prototype.reverse')

console.log('CASE infertir el orden de animals')

var animals = ['perro', 'gato', 'conejo', 'raton']
console.log(animals.reverse())
// [ 'raton', 'conejo', 'gato', 'perro' ]


console.log('CASE var devuelve el resultado')
console.log(animals)
// [ 'raton', 'conejo', 'gato', 'perro' ]






